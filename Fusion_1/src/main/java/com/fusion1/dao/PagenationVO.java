package com.fusion1.dao;

public class PagenationVO {

	
	// 페이지 번호
	private int page_no;
	
	// 총 게시물의 수
	private int totalCount;
	
	// 몇개씩 보여주고 싶은가?
	private int pageList;
	
	// 시작 페이지
	private int startPage;
	
	// 끝 페이지
	private int endPage;
	
	// mysql 수식용
	private int pageCon;
	
	// 검색용
	private String keyword;
	
	// 검색 형태
	private String searchCon;
	
	
	public int getPage_no() {
		return page_no;
	}

	public void setPage_no(int page_no) {
		this.page_no = page_no;
	}

	public int getTotalCount() {
		return totalCount;
	}
	
	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}
	
	public int getPageList() {
		return pageList;
	}
	
	public void setPageList(int pageList) {
		this.pageList = pageList;
	}

	public int getStartPage() {
		return startPage;
	}

	public void setStartPage(int startPage) {
		this.startPage = startPage;
	}

	public int getEndPage() {
		return endPage;
	}

	public void setEndPage(int endPage) {
		this.endPage = endPage;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public int getPageCon() {
		return pageCon;
	}

	public void setPageCon(int pageCon) {
		this.pageCon = pageCon;
	}
	
	public String getSearchCon() {
		return searchCon;
	}

	public void setSearchCon(String searchCon) {
		this.searchCon = searchCon;
	}

	@Override
	public String toString() {
		return "PagenationVO [page_no=" + page_no + ", totalCount=" + totalCount + ", pageList=" + pageList
				+ ", startPage=" + startPage + ", endPage=" + endPage + ", pageCon=" + pageCon + ", keyword=" + keyword
				+ ", searchCon=" + searchCon + "]";
	}

	
}
